import 'package:flutter/cupertino.dart';

class Labour {
  String name;
  String imageUrl;
  String location;
  String experience;
  String id;
  String price;
  String mobile;
  String age;

  Labour({
    required this.name,
    required this.imageUrl,
    required this.location,
    required this.id,
    required this.experience,
    required this.price,
    required this.mobile,
    required this.age,
  });
}
